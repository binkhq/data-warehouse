WITH lc_metrics_retailer AS (
    SELECT 
        *
        ,'LC_RETAILER_CHANNEL' AS TAB
    FROM {{ref('lc__links_joins__daily_retailer_channel')}}
    WHERE CHANNEL = 'LLOYDS'
)

,lc_metrics AS (
    SELECT 
        *
        ,'LC_CHANNEL' AS TAB
    FROM {{ref('user__loyalty_card__daily_channel_brand')}}
    WHERE CHANNEL = 'LLOYDS'
)

,trans AS (
    SELECT
        *
        ,'TRANS' AS TAB
    FROM {{ref('user__transactions__daily_user_level')}}
    WHERE CHANNEL = 'LLOYDS'
)

,users_metrics AS (
    SELECT
        *
        ,'USERS' AS TAB
    FROM {{ref('user__registrations__daily__channel_brand')}}
    WHERE CHANNEL = 'LLOYDS'
)

,metric_select AS (
    SELECT
        TAB
        ,DATE
        ,CHANNEL
        ,BRAND
        ,LOYALTY_PLAN_COMPANY
        ,LC001__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,NULL AS U003__USERS_WITH_A_LINKED_LOYALTY_CARD__DAILY_CHANNEL_BRAND__PIT
        ,LC004__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,LC005__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,LC008__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,NULL AS U005__REGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,NULL AS U006__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,NULL AS U001__REGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS U002__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS SPEND
        ,NULL AS U007__ACTIVE_USERS__USER_LEVEL_DAILY__NULL
        ,NULL AS TRANSACTIONS
    FROM
        lc_metrics_retailer

    UNION ALL

    SELECT
        TAB
        ,DATE
        ,CHANNEL
        ,BRAND
        ,NULL AS LOYALTY_PLAN_COMPANY
        ,NULL AS LC001__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,U003__USERS_WITH_A_LINKED_LOYALTY_CARD__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS LC004__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,NULL AS LC005__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,NULL AS LC008__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,NULL AS U005__REGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,NULL AS U006__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,NULL AS U001__REGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS U002__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS SPEND
        ,NULL AS U007__ACTIVE_USERS__USER_LEVEL_DAILY__NULL
        ,NULL AS TRANSACTIONS
    FROM
        lc_metrics    

    UNION ALL

    SELECT
        TAB
        ,DATE
        ,CHANNEL
        ,BRAND
        ,LOYALTY_PLAN_COMPANY
        ,NULL AS LC001__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,NULL AS U003__USERS_WITH_A_LINKED_LOYALTY_CARD__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS LC004__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,NULL AS LC005__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,NULL AS LC008__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,NULL AS U005__REGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,NULL AS U006__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,NULL AS U001__REGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS U002__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,SPEND
        ,U007__ACTIVE_USERS__USER_LEVEL_DAILY__NULL
        ,TRANSACTIONS
    FROM    
        trans

    UNION ALL

    SELECT
        TAB
        ,DATE
        ,CHANNEL
        ,BRAND
        ,NULL AS LOYALTY_PLAN_COMPANY
        ,NULL AS LC001__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,NULL AS U003__USERS_WITH_A_LINKED_LOYALTY_CARD__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS LC004__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__PIT
        ,NULL AS LC005__SUCCESSFUL_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,NULL AS LC008__DELETED_LOYALTY_CARDS__DAILY_CHANNEL_BRAND_MERCHANT__COUNT
        ,U005__REGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,U006__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__COUNT
        ,U001__REGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,U002__DEREGISTERED_USERS__DAILY_CHANNEL_BRAND__PIT
        ,NULL AS SPEND
        ,NULL AS U007__ACTIVE_USERS__USER_LEVEL_DAILY__NULL
        ,NULL AS TRANSACTIONS
    FROM
        users_metrics
)

select * from metric_select